package com.verill.serial;

public class Student {
	
	private int num;
	private String name;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Student(int num, String name) {
		super();
		this.num = num;
		this.name = name;
	}
	
	

}
